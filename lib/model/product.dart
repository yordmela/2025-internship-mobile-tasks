class Product {
  final String  id;
  final String name;
  final String price;
  final String category;
  final String rating;
  final String imagePath;
  final String description;

  Product({
    required this.id,
    required this.name,
    required this.price,
    required this.category,
    required this.rating,
    required this.imagePath,
    required this.description,
  });

}